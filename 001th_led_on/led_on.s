/**********************************************************************************************************
 @Function			void
 @Description			点亮LED : GPF4
 @Input				void
 @Return				void
**********************************************************************************************************/
.text
.global _start

_start:

/* -配置GPF4为输出引脚- 
 * -把 0x00000100 写到地址 0x56000050- 
 */
		ldr	r1,		=0x56000050
		ldr	r0,		=0x00000100
		str	r0,		[r1]

/* -配置GPF4输出低电平-
 * -把 0x00000000 写到地址 0x56000054-
 */
		ldr	r1,		=0x56000054
		ldr	r0,		=0x00000000
		str	r0,		[r1]

halt:
/* -死循环- */
		b	halt

