.text
.global _start

_start:

/* -设置内存: SP栈- */
		ldr	sp,		=4096											/* nand启动 */
;		ldr	sp,		=0x40000000+4096									/* nor启动 */

/* -调用函数- */
		mov	r0,		#4
		bl	led_on

		ldr	r0,		=100000
		bl	delay

		mov	r0,		#5
		bl	led_on

		ldr	r0,		=100000
		bl	delay

		mov	r0,		#6
		bl	led_on

halt:
/* -死循环- */
		b	halt

